#include "get_next_line.h"

int		simbol_check(char simb)
{
	if (!(simb == '0' || simb == '2' || simb == 'N' || simb == 'E'
	|| simb == 'S' || simb == 'W' || simb == '1' || simb == ' '))
	{
		write(1, "Error\n", 6);
		return (1);
	}
	return (0);
}
