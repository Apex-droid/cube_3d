#include "get_next_line.h"

int		contains_n(char *y)
{
	int h;

	h = 0;
	if (y == NULL)
		return (-1);
	while (y[h] != '\n' && y[h] != '\0')
		h++;
	if (y[h] == '\n')
		return (h);
	else
		return (-1);
}
