#include "get_next_line.h"
#include "./minilibx_mms/mlx.h"

void	free_buff(char **buff)
{
	int i;

	i = ft_strlen(*buff);
	while (i--)
	{
		*(*buff + i) = '\0';
	}
	free(*buff);
	buff = NULL;
}
