void	hook_strave(int *keycode)
{
	if (*keycode == 0)
	{
		g_button.a = 1;
		g_button.d = 0;
	}
	else if (*keycode == 2)
	{
		g_button.d = 1;
		g_button.a = 0;
	}
}
