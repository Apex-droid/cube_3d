#include "get_next_line.h"

int		ft_check_wall_y(char **kart, int y, int x)
{
	int k;
	int m;

	m = 0;
	k = 0;
	while (kart[m] != '\0' && kart[m + 1] != '\0' &&
		(ft_strlen(kart[m]) > (size_t)x || m <= y))
	{
		if ((k == 0 && kart[m][x] == '1' && m < y)
		|| (k == 1 && kart[m][x] == '1' && m > y))
			k++;
		m++;
	}
	return (k);
}
