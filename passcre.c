char	*passcre(int i, char **mapa)
{
	char *pass;

	pass = (char*)malloc(sizeof(char) * (i + 1));
	pass[i] = '\0';
	while (i)
	{
		i--;
		pass[i] = *(*mapa + i);
	}
	return (pass);
}
