#include "get_next_line.h"

int		ft_check_wall_x(char *kart, int x)
{
	int m;
	int k;

	m = 0;
	k = 0;
	while (kart[m] != '\0')
	{
		if ((kart[m] == '1' && k == 0 && m < x)
		|| (kart[m] == '1' && k == 1 && m > x))
			k++;
		m++;
	}
	return (k);
}
